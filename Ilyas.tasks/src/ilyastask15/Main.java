package ilyastask15;

public class Main {
    public static void main(String[] args) {
        float s = 184.7f;
        float pi = 3.14f;
        double g = s/pi;
        double r = Math.sqrt(g);
        double d = r*2;
        double l = 2*pi*r;
        System.out.println("Диаметр = "+ d);
        System.out.println("Длина окружности равна "+ l);
    }
}
